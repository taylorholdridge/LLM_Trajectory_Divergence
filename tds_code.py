"""### **Trajectory Divergence Score (TDS) Metric Framework**:

This gives the absolute value of the difference between embeddings with original and perturbation across layers and samples.

tds_mean: average euclidian distance across all layers

tds_max: largest deviation at any layer

tds_final: difference at last layer before prediction (closest to model's final decision states)

tds_auc (area under curve): approximates integral of divergence over layers, measuring cumulative reasoning deviation
"""

def trajectory_divergence_score(distances):
    """
    distances: array of layer-wise embedding distances
    Returns a dictionary of divergence metrics
    """
    return {
        "tds_mean": np.mean(distances),
        "tds_max": np.max(distances),
        "tds_final": distances[-1],          # final layer divergence
        "tds_auc": np.trapz(distances)       # area under curve
    }

for r in filtered_results:
    dist = trajectory_distance(r["emb_orig"], r["emb_pert"])
    r["trajectory_score"] = dist.mean()   # or max()

for r in filtered_results:
    dist = trajectory_distance(r["emb_orig"], r["emb_pert"])

    tds = trajectory_divergence_score(dist)

    r["trajectory_score"] = tds["tds_mean"]
    r["tds_mean"] = tds["tds_mean"]
    r["tds_max"] = tds["tds_max"]
    r["tds_final"] = tds["tds_final"]
    r["tds_auc"] = tds["tds_auc"]

