"""function to forward-pass prompts and extract hidden states


"""

def run_model(text):
    inputs = tokenizer(text, return_tensors="pt").to(model.device)

    with torch.no_grad():
        outputs = model(**inputs)

    logits = outputs.logits[:, -1, :]   # next-token logits
    hidden_states = outputs.hidden_states

    # Token IDs for YES / NO
    yes_id = tokenizer.encode("YES", add_special_tokens=False)[0]
    no_id  = tokenizer.encode("NO", add_special_tokens=False)[0]

    yes_logit = logits[0, yes_id].item()
    no_logit = logits[0, no_id].item()

    prediction = "YES" if yes_logit > no_logit else "NO" #answer is based on probability of next logit

    return {
        "hidden_states": hidden_states,
        "prediction": prediction,
        "yes_logit": yes_logit,
        "no_logit": no_logit
    }

"""compute the cosine similarity across layers (similarity of last token)"""

import torch.nn.functional as F

def compute_layer_drift(hidden_orig, hidden_pert):
    drift = []

    for layer in range(len(hidden_orig)):
        h1 = hidden_orig[layer][:, -1, :]  # last token
        h2 = hidden_pert[layer][:, -1, :]

        cos_sim = F.cosine_similarity(h1, h2).item()
        drift.append(1 - cos_sim)

    return drift

"""cosine similarity between each token in original vs. perturbed across layers"""

def compute_token_drift(hidden_orig, hidden_pert):
    num_layers = len(hidden_orig)

    drift_matrix = []

    for layer in range(len(hidden_orig)):
        # get min length per layer (safe)
        seq_len = min(
            hidden_orig[layer].shape[1],
            hidden_pert[layer].shape[1]
        )

        layer_drift = []

        for t in range(seq_len):
            h1 = hidden_orig[layer][:, t, :]
            h2 = hidden_pert[layer][:, t, :]

            cos_sim = torch.nn.functional.cosine_similarity(h1, h2).item()
            layer_drift.append(1 - cos_sim)

        drift_matrix.append(layer_drift)

    return drift_matrix

"""take in hidden state and then returns the embedding vector of last token, which contains the context of the prompt"""

def extract_last_token_embeddings(hidden_states):
    embeddings = []

    for layer_idx in range(len(hidden_states)):
        layer = hidden_states[layer_idx]

        vec = (
            layer[:, -1, :]
            .to(torch.float32)
            .squeeze()
            .cpu()
            .numpy()
        )

        embeddings.append(vec)

    return np.array(embeddings)

